char	*ft_strcat(char *dest, const char *src)
{
	int l1;
	int l2;
	int i;

	l1 = 0;
	l2 = 0;
	while (dest[l1] != 0)
		l1++;
	while (src[l2] != 0)
		l2++;
	i = 0;
	while (i <= l2)
	{
		dest[l1 + i] = src[i];
		i++;
	}
	return (dest);
}

char	*ft_strcpy(char *dest, const char *src)
{
	int l1;

	l1 = 0;
	while (src[l1] != '\0')
	{
		dest[l1] = src[l1];
		l1++;
	}
	dest[l1] = 0;
	return (dest);
}
